const DICTS: Record<Language, Dict> = { pl, en };

function interpolate(text: string, vars?: Vars): string {
  if (!vars) return text;
  return text.replace(/\{(\w+)\}/g, (_, k) => String(vars[k] ?? `{${k}}`));
}

/** Translate a key for the given language, with optional {placeholder} vars. */
export function translate(lang: Language, key: string, vars?: Vars): string {
  const text = DICTS[lang][key] ?? DICTS.en[key] ?? key;
  return interpolate(text, vars);
}

export type Translate = (key: string, vars?: Vars) => string;
